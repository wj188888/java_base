package com.wangjie.base.operator;
public class Demo06 {
    public static void main(String[] args) {
        /*
        位运算：
        A = 1101 0010
        B = 0011 0110

        A&B
        A|B
        A^B
        ~B
         */
        System.out.println(2<<3);
        System.out.println(2>>3);
        // <<座椅： *2； >>右移： /2
    }
}
